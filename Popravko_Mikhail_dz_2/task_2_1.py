"""
1. Выяснить тип результата выражений:
15 * 3
15 / 3
15 // 2
15 ** 2
"""

result = 15 * 3
print("Тип результата выражения 15 * 3 - ", type(result))

result = 15 / 3
print("Тип результата выражения 15 / 3 - ", type(result))

result = 15 // 2
print("Тип результата выражения 15 // 2 - ", type(result))

result = 15 ** 2
print("Тип результата выражения 15 ** 2 - ", type(result))
